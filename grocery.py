#Functions
def price_check(item_name): #Checks price of items
    global status
    for i in range(len(items)):
        if item_name == items[i]:
            status = "Found"
            print(prices[i])
    if status == "Error":
        print("Item not found, please try again.")
    else:
        status = "Error"

def discount_adder(discount): #Checks discounts on items
    global prices
    for i in range(len(prices)):
        prices[i] = round(prices[i] * (1 - discount), 2)

def price_matcher(max_price): #Price matcher
    for i in range(len(prices)):
        if prices[i] <= max_price:
            maximum.append(items[i])
